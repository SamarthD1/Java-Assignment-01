class identifierexample 
{
    public static void main(String[] args)  // 'Student' is a class identifier
    { 
    int age = 20;
    displayAge(age);       // 'age' is a variable identifier
    }
    public static void displayAge(int age) // 'displayAge' is a method identifier
    { 
        System.out.println(age);
    }
}
    

